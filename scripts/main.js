/* Open */

function openNav() {
   document.getElementById("myNav").style.display = "block";
   document.getElementById("beforeNav").style.display = "none";
   document.body.style.background = "#1c4ca4";
}

/* Close */
function closeNav() {
   document.getElementById("myNav").style.display = "none";
   document.getElementById("beforeNav").style.display = "block";
   document.body.style.background = "#ffffff";
}
